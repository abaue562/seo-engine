"""Autonomous Content Personas — multiple distinct voices for distributed publishing.

One strategy → many personas → varied expressions → broader reach → stronger signals.

Each persona has:
  - Unique tone, vocabulary, and expertise
  - Assigned channels (where they post)
  - History tracking (continuity)
  - Reputation score (boost high performers)
  - Variation enforcement (no duplicate content)
"""

from __future__ import annotations

import json
import logging
from datetime import datetime
from pydantic import BaseModel, Field

from core.claude import call_claude

log = logging.getLogger(__name__)


class Persona(BaseModel):
    """One distinct voice in the network."""
    id: str
    name: str
    tone: str                          # clear,cost-focused / visual,aspirational / authoritative,local
    expertise: str                     # home improvement / aesthetics / local installations
    goals: list[str] = []             # save money / upgrade look / build trust
    channels: list[str] = []          # reddit, quora, blog, medium, instagram, gbp
    post_count: int = 0
    avg_engagement: float = 0.0
    reputation: float = 5.0           # 0-10
    active: bool = True


class PersonaOutput(BaseModel):
    """Content generated by a specific persona."""
    persona_id: str
    persona_name: str
    channel: str
    tone: str
    title: str = ""
    content: str = ""
    hashtags: list[str] = []
    link_strategy: str = "soft"


class PersonaCampaign(BaseModel):
    """Multi-persona campaign for one topic."""
    keyword: str
    outputs: list[PersonaOutput] = Field(default_factory=list)
    personas_used: int = 0
    channels_covered: list[str] = []


