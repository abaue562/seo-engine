PERSONA_PROMPT = """You are writing as a SPECIFIC persona. Your voice must be DISTINCT from other personas.

PERSONA: {persona_name}
Tone: {tone}
Expertise: {expertise}
Goals: {goals}
Target channel: {channel}

TOPIC: {keyword}
Business: {business_name}
City: {city}

Write content for the {channel} platform AS this persona. The content must:
- Match the persona's tone EXACTLY (not generic)
- Focus on the persona's goals and expertise
- Be appropriate for the {channel} platform's norms
- Be about "{keyword}" in {city}
- NOT sound like marketing copy — sound like a real person with this perspective

Return ONLY JSON:
{{
  "title": "post/article title",
  "content": "the full content (200-500 words depending on platform)",
  "hashtags": ["relevant", "hashtags"],
  "link_strategy": "soft | none"
}}"""


class PersonaSystem:
    """Manages personas and generates multi-voice content."""

    def __init__(self, personas: list[Persona] | None = None):
        self.personas = {p.id: p for p in (personas or DEFAULT_PERSONAS)}

    async def generate_campaign(
        self,
        keyword: str,
        business_name: str,
        city: str,
        max_personas: int = 3,
    ) -> PersonaCampaign:
        """Generate content from multiple personas for one topic."""
        campaign = PersonaCampaign(keyword=keyword)
        channels_used: set[str] = set()

        # Pick personas — prioritize by reputation, ensure channel diversity
        active = sorted(
            [p for p in self.personas.values() if p.active],
            key=lambda p: p.reputation,
            reverse=True,
        )[:max_personas]

        for persona in active:
            # Pick the best channel for this persona (one they haven't used + we haven't covered)
            available = [c for c in persona.channels if c not in channels_used]
            if not available:
                available = persona.channels[:1]
            channel = available[0]

            try:
                output = await self._generate_for_persona(persona, channel, keyword, business_name, city)
                if output:
                    campaign.outputs.append(output)
                    channels_used.add(channel)
            except Exception as e:
                log.error("persona.generate_fail  persona=%s  err=%s", persona.name, e)

        campaign.personas_used = len(campaign.outputs)
        campaign.channels_covered = list(channels_used)

        log.info("persona.campaign  keyword=%s  personas=%d  channels=%s",
                 keyword, campaign.personas_used, campaign.channels_covered)

        return campaign

    async def _generate_for_persona(
        self,
        persona: Persona,
        channel: str,
        keyword: str,
        business_name: str,
        city: str,
    ) -> PersonaOutput | None:
        """Generate content for one persona on one channel."""
        prompt = PERSONA_PROMPT.format(
            persona_name=persona.name,
            tone=persona.tone,
            expertise=persona.expertise,
            goals=", ".join(persona.goals),
            channel=channel,
            keyword=keyword,
            business_name=business_name,
            city=city,
        )

        raw = call_claude(
            prompt,
            system=f"You ARE the '{persona.name}' persona. Write ONLY in their voice. Return ONLY valid JSON.",
            max_tokens=1500,
        )

        if raw.startswith("```"):
            raw = raw.split("\n", 1)[1].rsplit("```", 1)[0].strip()
        start = raw.find("{")
        if start > 0:
            raw = raw[start:]

        try:
            data = json.loads(raw)
            output = PersonaOutput(
                persona_id=persona.id,
                persona_name=persona.name,
                channel=channel,
                tone=persona.tone,
                title=data.get("title", ""),
                content=data.get("content", ""),
                hashtags=data.get("hashtags", []),
                link_strategy=data.get("link_strategy", "soft"),
            )

            persona.post_count += 1
            log.info("persona.generated  persona=%s  channel=%s  len=%d",
                     persona.name, channel, len(output.content))
            return output

        except Exception as e:
            log.error("persona.parse_fail  persona=%s  err=%s", persona.name, e)
            return None

    def boost_performer(self, persona_id: str, engagement: float):
        """Increase reputation for high-performing personas."""
        p = self.personas.get(persona_id)
        if p:
            p.avg_engagement = (p.avg_engagement * (p.post_count - 1) + engagement) / max(p.post_count, 1)
            if engagement > 5:
                p.reputation = min(10, p.reputation + 0.5)
            elif engagement < 2:
                p.reputation = max(1, p.reputation - 0.3)

    def get_summary(self) -> list[dict]:
        return [
            {
                "id": p.id, "name": p.name, "tone": p.tone,
                "channels": p.channels, "posts": p.post_count,
                "reputation": p.reputation, "active": p.active,
            }
            for p in self.personas.values()
        ]
